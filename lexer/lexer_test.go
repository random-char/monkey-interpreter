func checkTests(l *Lexer, tests []nextTokenTest) error {
	for i, tt := range tests {
		tok := l.NextToken()

		if tok.Type != tt.expectedType {
			return fmt.Errorf("tests[%d] - tokentype wrong. expected=%q, got=%q",
				i, tt.expectedType.String(), tok.Type.String())
		}

		if tok.Literal != tt.expectedLiteral {
			return fmt.Errorf("tests[%d] - literal wrong. expected=%q, got=%q",
				i, tt.expectedLiteral, tok.Literal)
		}
	}

	return nil
}
