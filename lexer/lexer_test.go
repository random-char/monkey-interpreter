import (
	"fmt"
	"testing"

	"monkey-interpreter/token"
)

type nextTokenTest struct {
	expectedType    token.TokenType
	expectedLiteral string
}

func TestNextToken(t *testing.T) {
	input := `=+(){},;`

	tests := []nextTokenTest{
		{token.ASSIGN, "="},
		{token.PLUS, "+"},
		{token.LPAREN, "("},
		{token.RPAREN, ")"},
		{token.LBRACE, "{"},
		{token.RBRACE, "}"},
		{token.COMMA, ","},
		{token.SEMICOLON, ";"},
	}

	l := New(input)
	if err := checkTests(l, tests); err != nil {
		t.Fatal(err.Error())
	}
}

